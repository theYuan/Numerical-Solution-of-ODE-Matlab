clear;
clc;
a=0;b=1;n1=60;n2=120;
ua=1;ub=exp(1);
coef=[-1,1,2]
[x1,U1]=two_point('f',coef,a,b,n1,ua,ub)
[x2,U2]=two_point('f',coef,a,b,n2,ua,ub)
u1=exp(x1.^2)'
u2=exp(x2.^2)'
%%%%%
subplot(2,2,1)
plot(x1,U1,'-o',x1,u1)
title('60等分')
xlabel('x')
ylabel('u')
%%%%%
subplot(2,2,2)
plot(x2,U2,'-o',x2,u2)
title('120等分')
xlabel('x')
ylabel('u')
%plot error
subplot(2,2,3)
plot(x1,abs(u1-U1))
xlabel('x')
ylabel('error')
title('60网格误差')
subplot(2,2,4)
plot(x2,abs(u2-U2))
xlabel('x')
ylabel('error')
title('120网格误差')
norm(U1-u1,inf)
norm(U2-u2,inf)